int	ft_sqrt(int nb)
{
	int	i;
	int	j;

	i = 1;
	j = 0;
	while (nb - i >= 0)
	{
		nb = nb - i;
		i += 2;
		j++;
	}
	if (nb == 0)
		return (j);
	else
		return (0);
}
